    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
         try {
             int cant=AddSala.codSala();
             AddSala.restar();
             
             if(cant==0){
                JOptionPane.showMessageDialog(null, "Sin Salas");
                this.setVisible(false);
            }
             
           Object [][] data = new Object[cant][5];  
           for(int x=0;x<cant;x++){    
             Sala act=Menu.getSala(x+1);
              
             if(act!=null){
                 data[x][0]=act.getCod();
                 data[x][1]=act.getTipo();
                 data[x][2]=act.getFilas();
                 data[x][3]=act.getAsientos();
                 data[x][4]=act.getPrecio();
             }
             
          }      
        
          String [] cadenas={ "Codigo", "Tipo", "Filas","Columnas","Precio"};        
          jTable1.setModel(new javax.swing.table.DefaultTableModel( data  ,cadenas)) ;
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_formWindowOpened

